//    Задание 6: "Конвертация температуры"
//    Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты и наоборот в зависимости от указанной единицы измерения (C/F).
//    Единицу измерения нужно передать вторым аргументом функции. Несколько аргументов передаются через запятую.
//    Распечатай в консоль результат конвертации с добавлением единицы измерения.
//    Чтобы добавить единицу измерения после результата используй функцию печати без переноса строки print("C") или print("F").

fun example6(value: Double, unit: String) {
    when (unit) {
        "C" -> {
            val result = value * 9 / 5 + 32
            print(result)
            println("F")
        }
        "F" -> {
            val result = (value - 32) * 5 / 9
            print(result)
            println("C")
        }
        else -> println("Некорректная единица измерения")
    }
}

//    Задание 7: "Подбор одежды по погоде"
//    Напишите функцию, которая на основе температуры воздуха рекомендует тип одежды:
//    "куртка и шапка" при температуре ниже +10, "ветровка" от +10 до +18 градусов включительно
//    и "футболка и шорты" при температуре выше +18 градусов. При температурах ниже -30 и выше +35 рекомендуйте не выходить из дома.

fun example7(temp: Int) {
    when {
        temp < -30 || temp > 35 -> println("лучше не выходить из дома")
        temp in -30 until 10 -> println("куртка и шапка")
        temp in 10..18 -> println("ветровка")
        else -> println("футболка и шорты")
    }
}

//    Задание 8: "Выбор фильма по возрасту"
//    Кинотеатр предлагает фильмы разных возрастных категорий. Напишите функцию, которая принимает возраст зрителя
//    и возвращает доступные для него категории фильмов: "детские" (от 0 до 9), "подростковые" (от 10 до 18), "18+" для остальных.

fun example8(age: Int) {
    when (age) {
        in 0..9 -> println("детские")
        in 10 until 18 -> println("подростковые")
        else -> println("18+")
    }
}