    }
}

//    Задание 4: "Расчет бонусных баллов"
//    Клиенты интернет-магазина получают бонусные баллы за покупки. Напишите функцию, которая принимает сумму покупки
//    и печатает в консоль количество бонусных баллов: 2 балла за каждые 100 рублей при сумме покупки до 1000 рублей
//    и 3 балла за каждые 100 рублей при сумме свыше этого.

fun example4(amount: Int) {
    val points = if (amount <= 1000) {
        (amount / 100) * 2
    } else {
        (amount / 100) * 3
    }
    println(points)
}

//    Задание 5: "Определение типа документа"
